//
    //  PNGVariantOracleTests.swift
    //  PureDraw
//
    //  PNG decoding is lossless, so PureDraw and CoreGraphics decoding the SAME PNG must agree exactly.
    //  These pin the bit-depth and interlace support added to the decoder: 1-bit grayscale, 16-bit
    //  grayscale (scaled to 8-bit with rounding), 4-bit palette, 8-bit interlaced (Adam7), and the
    //  gnarliest combination, 4-bit palette interlaced. Each fixture is a minimal PNG produced offline.
//

    @testable import Core
    import CoreGraphics
    import Foundation
    import ImageIO
    import Testing

    struct PNGVariantOracleTests {
        private func decodeWithCoreGraphics(_ png: [UInt8], width: Int, height: Int) -> [UInt8]? {
            guard let source = CGImageSourceCreateWithData(Data(png) as CFData, nil),
                  let image = CGImageSourceCreateImageAtIndex(source, 0, nil)
            else { return nil }
            var buffer = [UInt8](repeating: 0, count: width * height * 4)
            guard let ctx = buffer.withUnsafeMutableBytes({ raw in
                CGContext(
                    data: raw.baseAddress, width: width, height: height, bitsPerComponent: 8, bytesPerRow: width * 4,
                    space: CGColorSpaceCreateDeviceRGB(), bitmapInfo: CGImageAlphaInfo.premultipliedLast.rawValue
                )
            }) else { return nil }
            ctx.draw(image, in: CGRect(x: 0, y: 0, width: width, height: height))
            return buffer
        }

        private func premultiplied(_ straight: [UInt8]) -> [UInt8] {
            var out = straight
            for p in stride(from: 0, to: out.count, by: 4) {
                let alpha = Double(out[p + 3]) / 255
                out[p] = UInt8(Double(out[p]) * alpha + 0.5)
                out[p + 1] = UInt8(Double(out[p + 1]) * alpha + 0.5)
                out[p + 2] = UInt8(Double(out[p + 2]) * alpha + 0.5)
            }
            return out
        }

        private func expectExactMatch(_ png: [UInt8], _ width: Int, _ height: Int, _ label: String) throws {
            let mine = try ImageDecoder.decode(png)
            #expect(mine.width == width && mine.height == height, "\(label) dimensions")
            let reference = try #require(decodeWithCoreGraphics(png, width: width, height: height), "\(label) CG decode")
            #expect(premultiplied(mine.data) == reference, "\(label) must match CoreGraphics exactly")
        }

        @Test func decodesGrayscale1BitLikeCoreGraphics() throws {
            try expectExactMatch(Fixtures.gray1Bit, 8, 6, "1-bit grayscale")
        }

        @Test func decodesGrayscale16BitLikeCoreGraphics() throws {
            try expectExactMatch(Fixtures.gray16Bit, 8, 6, "16-bit grayscale")
        }

        @Test func decodesPalette4BitLikeCoreGraphics() throws {
            try expectExactMatch(Fixtures.palette4Bit, 8, 6, "4-bit palette")
        }

        @Test func decodesInterlacedTruecolorLikeCoreGraphics() throws {
            try expectExactMatch(Fixtures.interlaced8Bit, 8, 6, "8-bit interlaced (Adam7)")
        }

        @Test func decodesInterlacedPalette4BitLikeCoreGraphics() throws {
            try expectExactMatch(Fixtures.interlacedPalette4Bit, 8, 6, "4-bit palette interlaced")
        }
    }

